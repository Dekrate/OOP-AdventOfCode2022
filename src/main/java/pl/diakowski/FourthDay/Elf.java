package pl.diakowski.FourthDay;

public class Elf {
    private int begin;
    private int end;

    public Elf(int begin, int end) {
        this.begin = begin;
        this.end = end;
    }

    public Elf() {
    }

    public int getBegin() {
        return begin;
    }

    public int getEnd() {
        return end;
    }
}
